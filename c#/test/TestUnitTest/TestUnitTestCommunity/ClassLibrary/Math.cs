namespace ClassLibrary
{
    public class Math
    {
        public int Add(int leftOp, int rightOp)
        {
            return leftOp + rightOp;
        }

        public int Sub(int leftOp, int rightOp)
        {
            return leftOp - rightOp;
        }

        public int Mul(int leftOp, int rightOp)
        {
            return leftOp * rightOp;
        }

        public int Div(int leftOp, int rightOp)
        {
            return leftOp / rightOp;
        }
    }
}
